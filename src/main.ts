async function bootstrap() {
  const app = await NestFactory.create<NestFastifyApplication>(
    AppModule,
    new FastifyAdapter(),
  );

  const maxFileSizeMb = env.MAX_FILE_SIZE_MB || 5;
  await app.register(fastifyMultipart as any, {
    limits: {
      fileSize: maxFileSizeMb * 1024 * 1024,
    },
  });

  const port = process.env.PORT || 4000;
  const apiPrefix = process.env.API_PREFIX || 'api/v1';

  app.setGlobalPrefix(apiPrefix);
  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
    }),
  );

  await app.listen(port, '0.0.0.0');

  console.log(BANNER);
  console.log(`ūüöÄ Server is running on: http://localhost:${port}/${apiPrefix}`);
  console.log(`ūüďö Environment: ${process.env.NODE_ENV || 'development'}`);
  console.log(`‚Źį Started at: ${new Date().toISOString()}\n`);
}

bootstrap();

